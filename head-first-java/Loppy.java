public class Loppy{

	public static void main(String[] args){
	
		int x = 1;
		System.out.println("Before the loop");
		
		while(x < 9){
			System.out.println("Into the loop");
			System.out.println("value of x: " + x);
			x++;
		}
		
		System.out.println("this after the loop");
	
	}

}